    public function actionViewPage($id_page)
    {
        $page = StatisPage::find()->where(['id_page' => $id_page])->one();
        if (!empty($page)) {
            return $this->redirect([$page->url_page]);
        } else {
            return $this->redirect('index');
        }
    }

