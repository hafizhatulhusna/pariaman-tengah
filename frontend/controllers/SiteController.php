    public function actionHubungiKami()
    {
        $model = new KirimPesan();

        $model->tanggal_kirim = date('Y-m-d');

        if ($model->load(Yii::$app->request->post()) && $model->save()) {

            $model->tanggal_kirim = date('Y-m-d');

            Yii::$app->session->setFlash('success', '<b>Pesan anda sudah terkirim</b><br>');
